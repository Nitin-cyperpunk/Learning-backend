import chalk from 'chalk';

console.log(chalk.blue('Hello world!'));
console.log(chalk.green.italic('This is a green message!'));
console.log(chalk.red.bold('This is a bold red message!'));
console.log(chalk.yellow.underline('This is a underlined yellow message!'));
console.log(chalk.magenta.inverse('This is a inverse magenta message!'));
console.log(chalk.cyan.strikethrough('This is a strikethrough cyan message!'));
console.log(chalk.white.bgBlack('This is a white text on black background!'));